#  现在有字符串：msg = "ABCDEFGHIJ"
msg = "ABCDEFGHIJ"
#   1. 截取从 2 ~ 5 字符 的字符串
print(msg[1: 5])
#   2. 截取从 2 ~ 末尾 的字符串
print(msg[2:])
#   3. 截取从 开始 ~ 5 字符 的字符串
print(msg[:5])
#   4. 截取完整的字符串
print(msg[:])
#   5. 从开始位置，每隔一个字符截取字符串
print(msg[::2])
#   6. 从索引 1 开始，每隔一个取一个
print(msg[1::2])
#   7. 截取从 2 ~ 末尾 - 1 的字符串
print(msg[2:-1])
#   8. 截取字符串末尾两个字符
print(msg[-2:])
#   9. 字符串的逆序
print(msg[::-1])